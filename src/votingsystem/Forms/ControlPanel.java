    public void reloadElectionComboBox(int departmentId) {
        this.electionList = Election.getElectionUsingDepartmentId(departmentId);
        electionComboBox.setModel(new DefaultComboBoxModel(this.electionList.toArray()));
        
    }
    private void reloadPage() {
        
        Election election = (Election) electionComboBox.getSelectedItem();
        //reload combo box
        if(election != null) {
            if(election.getStatus().equals("ACTIVE")) {
                statusField.setForeground(Color.GREEN);
                statusField.setText(election.getStatus());
                managePartyListBtn.setEnabled(true);
                resultBtn.setEnabled(false);
                return;
            }else if(election.getStatus().equals("INACTIVE")) {
                statusField.setForeground(Color.RED);
                statusField.setText(election.getStatus());
                managePartyListBtn.setEnabled(false);
                resultBtn.setEnabled(true);
                return;
            }
        }
        resultBtn.setEnabled(false);
        managePartyListBtn.setEnabled(false);
        
        statusField.setText("");    
    }
