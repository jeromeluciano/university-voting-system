    private void deleteElectionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteElectionBtnActionPerformed
        // TODO add your handling code here:
        
        Election election = (Election) electionComboBox.getSelectedItem();
        Department department = (Department) departmentComboBox.getSelectedItem();
        if(election != null && department != null) {
            if(JOptionPane.showConfirmDialog(this,"Are you sure?") == 0) { 

                if(Election.delete(election.getId())) {
                    reloadElectionComboBox(department.getId());
                    reloadPage();
                    JOptionPane.showMessageDialog(this, "Succesfully Deleted");
                }
            }
        }
    }//GEN-LAST:event_deleteElectionBtnActionPerformed

