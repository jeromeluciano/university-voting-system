    private void electionComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_electionComboBoxActionPerformed
        // TODO add your handling code here:
        Election election = (Election) electionComboBox.getSelectedItem();
        if(election.getStatus().equals("ACTIVE")) {
            statusField.setForeground(Color.GREEN);
            statusField.setText(election.getStatus());
            managePartyListBtn.setEnabled(true);
        }else if(election.getStatus().equals("INACTIVE")) {
            statusField.setForeground(Color.RED);
            statusField.setText(election.getStatus());
            managePartyListBtn.setEnabled(false);
        }
    }//GEN-LAST:event_electionComboBoxActionPerformed

    private void startElectionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startElectionBtnActionPerformed
        // TODO add your handling code here:
        Election election = (Election) electionComboBox.getSelectedItem();
        Department department = (Department) departmentComboBox.getSelectedItem();
        String updateElection = "UPDATE election SET status = 'ACTIVE' WHERE id = " + election.getId();
        if(election != null && department != null) {
            if(DBConnection.updateQuery(updateElection) > 0) {
                int selectedIndex = electionComboBox.getSelectedIndex();
                JOptionPane.showMessageDialog(this, election.getName() + " is now ACTIVE");
                electionList = Election.getElectionUsingDepartmentId(department.getId());
                electionComboBox.setModel(new DefaultComboBoxModel(electionList.toArray()));
                electionComboBox.setSelectedIndex(selectedIndex);
                statusField.setText("ACTIVE");
                statusField.setForeground(Color.GREEN);
                managePartyListBtn.setEnabled(true);
            }else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }
        
    }//GEN-LAST:event_startElectionBtnActionPerformed

